#include <stdio.h> // C 语言的标准支持

#include <iostream> // C++的标准支持

using namespace std; // C++的命名空间，类似JAVA中的内部类

int main() {
    // C++语言面向对象：对象
    // C语言面向过程：函数+结构体
    // C++代码中，可以运行C语言和调用C语言，C++是C语言的超集

    // C++的打印
    std::cout << "Hello, World!" << std::endl;
    cout << "Hello, World!" << endl;

    // C语言的打印
    printf("Hello, World!\n");


    return 0;
}
